"use client"

import { useEffect } from "react"
import { useForm, useFieldArray, Controller } from "react-hook-form"
import { zodResolver } from "@hookform/resolvers/zod"
import {
    Dialog,
    DialogContent,
    DialogDescription,
    DialogFooter,
    DialogHeader,
    DialogTitle,
} from "@/components/ui/dialog"
import { Button } from "@/components/ui/button"
import { Input } from "@/components/ui/input"
import { Label } from "@/components/ui/label"
import { Textarea } from "@/components/ui/textarea"
import { Checkbox } from "@/components/ui/checkbox"
import { Plus, Trash2, Calendar, Clock } from "lucide-react"
import { createStudyPlan } from "@/lib/api/schedule"
import { createStudyPlanSchema } from "@/schema/study-plan"

export default function CreateScheduleDialog({ open, onOpenChange, onSuccess }) {
    const {
        register,
        control,
        handleSubmit,
        formState: { errors, isSubmitting },
        reset,
        setError: setFormError
    } = useForm({
        resolver: zodResolver(createStudyPlanSchema),
        defaultValues: {
            title: "",
            schedules: [
                {
                    startTime: "",
                    durationMin: "",
                    taskDesc: "",
                    syncToCalendar: false
                }
            ]
        }
    })

    const { fields, append, remove } = useFieldArray({
        control,
        name: "schedules"
    })

    // Reset form when dialog closes
    useEffect(() => {
        if (!open) {
            reset()
        }
    }, [open, reset])

    const onSubmit = async (data) => {
        try {
            // Convert datetime-local to ISO string
            const formattedSchedules = data.schedules.map((schedule) => ({
                startTime: new Date(schedule.startTime).toISOString(),
                durationMin: parseInt(schedule.durationMin, 10),
                taskDesc: schedule.taskDesc,
                syncToCalendar: schedule.syncToCalendar || false
            }))

            const result = await createStudyPlan({
                title: data.title.trim(),
                schedules: formattedSchedules
            })

            if (result.success) {
                reset()
                onOpenChange(false)
                onSuccess?.()
            } else {
                setFormError("root", {
                    type: "manual",
                    message: result.error || "Tạo kế hoạch học tập thất bại"
                })
            }
        } catch (err) {
            setFormError("root", {
                type: "manual",
                message: "Có lỗi xảy ra khi tạo kế hoạch học tập"
            })
        }
    }

    const handleAddSchedule = () => {
        append({
            startTime: "",
            durationMin: "",
            taskDesc: "",
            syncToCalendar: false
        })
    }

    return (
        <Dialog open={open} onOpenChange={onOpenChange}>
            <DialogContent className="max-w-2xl max-h-[90vh] overflow-y-auto">
                <DialogHeader>
                    <DialogTitle>Tạo kế hoạch học tập mới</DialogTitle>
                    <DialogDescription>
                        Tạo kế hoạch học tập chi tiết với các buổi học cụ thể
                    </DialogDescription>
                </DialogHeader>

                <form onSubmit={handleSubmit(onSubmit)} className="space-y-6">
                    {/* Title */}
                    <div className="space-y-2">
                        <Label htmlFor="title">
                            Tiêu đề kế hoạch <span className="text-red-500">*</span>
                        </Label>
                        <Input
                            id="title"
                            placeholder="Ví dụ: Complete TOEIC 650+ in 3 months"
                            {...register("title")}
                            disabled={isSubmitting}
                        />
                        {errors.title && (
                            <p className="text-sm text-red-500">{errors.title.message}</p>
                        )}
                    </div>

                    {/* Schedules */}
                    <div className="space-y-4">
                        <div className="flex items-center justify-between">
                            <Label>
                                Lịch học <span className="text-red-500">*</span>
                            </Label>
                            <Button
                                type="button"
                                variant="outline"
                                size="sm"
                                onClick={handleAddSchedule}
                                disabled={isSubmitting}
                            >
                                <Plus className="w-4 h-4 mr-2" />
                                Thêm lịch học
                            </Button>
                        </div>

                        {fields.map((field, index) => (
                            <div
                                key={field.id}
                                className="p-4 border rounded-lg space-y-3 bg-muted/40"
                            >
                                <div className="flex items-center justify-between mb-2">
                                    <span className="text-sm font-medium">
                                        Plan #{index + 1}
                                    </span>
                                    {fields.length > 1 && (
                                        <Button
                                            type="button"
                                            variant="ghost"
                                            size="sm"
                                            onClick={() => remove(index)}
                                            disabled={isSubmitting}
                                        >
                                            <Trash2 className="w-4 h-4 text-red-500" />
                                        </Button>
                                    )}
                                </div>

                                <div className="grid grid-cols-2 gap-3">
                                    <div className="space-y-2">
                                        <Label htmlFor={`schedules.${index}.startTime`}>
                                            <Calendar className="w-3 h-3 inline mr-1" />
                                            Thời gian bắt đầu
                                        </Label>
                                        <Input
                                            id={`schedules.${index}.startTime`}
                                            type="datetime-local"
                                            {...register(`schedules.${index}.startTime`)}
                                            disabled={isSubmitting}
                                        />
                                        {errors.schedules?.[index]?.startTime && (
                                            <p className="text-xs text-red-500">
                                                {errors.schedules[index].startTime.message}
                                            </p>
                                        )}
                                    </div>

                                    <div className="space-y-2">
                                        <Label htmlFor={`schedules.${index}.durationMin`}>
                                            <Clock className="w-3 h-3 inline mr-1" />
                                            Thời lượng (phút)
                                        </Label>
                                        <Input
                                            id={`schedules.${index}.durationMin`}
                                            type="number"
                                            min="1"
                                            placeholder="60"
                                            {...register(`schedules.${index}.durationMin`)}
                                            disabled={isSubmitting}
                                        />
                                        {errors.schedules?.[index]?.durationMin && (
                                            <p className="text-xs text-red-500">
                                                {errors.schedules[index].durationMin.message}
                                            </p>
                                        )}
                                    </div>
                                </div>

                                <div className="space-y-2">
                                    <Label htmlFor={`schedules.${index}.taskDesc`}>
                                        Mô tả nhiệm vụ
                                    </Label>
                                    <Textarea
                                        id={`schedules.${index}.taskDesc`}
                                        placeholder="Ví dụ: Làm riêng từng part bạn muốn luyện tập - Part 1 & 2"
                                        {...register(`schedules.${index}.taskDesc`)}
                                        disabled={isSubmitting}
                                        rows={2}
                                    />
                                    {errors.schedules?.[index]?.taskDesc && (
                                        <p className="text-xs text-red-500">
                                            {errors.schedules[index].taskDesc.message}
                                        </p>
                                    )}
                                </div>

                                {/* Sync to Google Calendar */}
                                <div className="flex items-center space-x-2">
                                    <Controller
                                        name={`schedules.${index}.syncToCalendar`}
                                        control={control}
                                        render={({ field }) => (
                                            <Checkbox
                                                id={`schedules.${index}.syncToCalendar`}
                                                checked={field.value}
                                                onCheckedChange={field.onChange}
                                                disabled={isSubmitting}
                                            />
                                        )}
                                    />
                                    <Label
                                        htmlFor={`schedules.${index}.syncToCalendar`}
                                        className="text-sm font-normal cursor-pointer"
                                    >
                                        Đồng bộ với Google Calendar
                                    </Label>
                                </div>
                            </div>
                        ))}
                    </div>

                    {/* Error message */}
                    {errors.root && (
                        <div className="text-sm text-red-500 bg-red-50 border border-red-200 rounded p-3">
                            {errors.root.message}
                        </div>
                    )}

                    <DialogFooter>
                        <Button
                            type="button"
                            variant="outline"
                            onClick={() => onOpenChange(false)}
                            disabled={isSubmitting}
                        >
                            Hủy
                        </Button>
                        <Button type="submit" disabled={isSubmitting}>
                            {isSubmitting ? "Đang tạo..." : "Tạo kế hoạch"}
                        </Button>
                    </DialogFooter>
                </form>
            </DialogContent>
        </Dialog>
    )
}
